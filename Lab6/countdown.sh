#!/bin/bash
# Проверяем, передан ли аргумент
if [ -z "$1" ]; then
    echo "Ошибка: Укажите число в качестве аргумента."
    exit 1
fi

# Проверяем, является ли аргумент числом
if ! [[ "$1" =~ ^[0-9]+$ ]]; then
    echo "Ошибка: Аргумент должен быть положительным целым числом."
    exit 1
fi

# Преобразуем аргумент в переменную
number=$1

# Выполняем обратный отсчёт
while [ "$number" -ge 0 ]; do
    echo "$number"
    number=$((number - 1))
    sleep 1  # Задержка в 1 секунду между числами (необязательно, но добавляет реализм)
done

echo "Обратный отсчёт завершён!"

