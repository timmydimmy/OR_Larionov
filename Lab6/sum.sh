#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ "$#" -ne 2 ]; then
    echo "Ошибка: Укажите два числа в качестве аргументов."
    echo "Пример: ./sum.sh 5 10"
    exit 1
fi

# Проверяем, что оба аргумента являются числами
if ! [[ "$1" =~ ^-?[0-9]+$ && "$2" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: Оба аргумента должны быть числами."
    exit 1
fi

# Суммируем аргументы
sum=$(( $1 + $2 ))

# Выводим результат
echo "Сумма чисел $1 и $2 равна $sum"
